    private boolean validarLogin() {
        
        if (campoNome.getText().trim().equals("")) {
            
            this.campoNome.requestFocus();
            Util.getAlert().alertNormal("Informe o Login!");
            
            return false;
        }
        if (campoSenha.getText().trim().equals("")) {
            
            this.campoSenha.requestFocus();
            Util.getAlert().alertNormal("Informe a Senha!");
            
            return false;
        }
        
        if (!campoNome.getText().trim().equals("admin") || !campoSenha.getText().trim().equals("admin")) {
            
            this.campoNome.setText("");
            this.campoSenha.setText("");
            this.campoNome.requestFocus();
            
            Util.getAlert().alertNormal("Login ou senha est√£o incorretos!");
            
            return false;
        }
        
        return true;
    }
