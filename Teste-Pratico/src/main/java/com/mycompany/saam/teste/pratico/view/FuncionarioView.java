    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String nome = tfNome.getText();
        String salario = ftfSalario.getText().replace(",", ".");
        String dataAdmissao = ftfData.getText();
        boolean status = cbStatus.isSelected();
        try {
            if (operacao == Operacao.INCLUIR) {
                if (funcionarioController.cadastrar(nome, dataAdmissao, salario, status)) {
                    JOptionPane.showMessageDialog(this, "Funcionario cadastrado com sucesso");
                    this.dispose();
                    new FuncionarioBrwView().setVisible(true);
                }
            }
            else if (operacao == Operacao.ALTERAR) {
                if (funcionarioController.alterar(nome, dataAdmissao, salario, status,funcionarioSelecionado.getId())) {
                    JOptionPane.showMessageDialog(this, "Funcionario alterado com sucesso");
                    this.dispose();
                    new FuncionarioBrwView().setVisible(true);
                }
            }
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        try {
            if (funcionarioController.deletar(funcionarioSelecionado.getId())){
                JOptionPane.showMessageDialog(this, "Funcionario exclu√≠do com sucesso");
                this.dispose();
                new FuncionarioBrwView().setVisible(true);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnDeletarActionPerformed

